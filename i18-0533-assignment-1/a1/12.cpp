#include <iostream>
using namespace std;
int main ()
{
  int num;
  cout<<"enter number from zero to six to indicate color\n";
  cin>>num;
  if (num==0)
  {
    cout<<"the colour is red\n";
  }
  if (num==1)
  {
    cout<<"the color is violet\n";
  }
  if (num==2)
  {
    cout<<"the color is white\n";
  }
  if (num==3)
  {
    cout<<"the color is yellow\n";
  }
  if (num==4)
  {
    cout<<"the color is pink\n";
  }
  if (num==5)
  {
    cout<<"the color is blue\n";
  }
  if (num==6)
  {
    cout<<"the color is green\n";
  }
  return 0;
}
